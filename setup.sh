```bash
#!/bin/bash

# Установка зависимостей
pip install -r requirements.txt

# Запуск бота
python main.py
```

## Использование

1. Введите команду `/start` для начала взаимодействия с ботом.
2. Используйте команды, такие как `/info`, `/subscribe`, чтобы взаимодействовать с активами и настраивать уведомления.